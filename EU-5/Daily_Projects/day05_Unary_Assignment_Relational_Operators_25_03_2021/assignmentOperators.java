package day05_Unary_Assignment_Relational_Operators_25_03_2021;

public class assignmentOperators {

	public static void main(String[] args) {
		
		
		int i = 100;
		System.out.println(i);
		
		i+=1;
		System.out.println(i);
		
		i-=2;
		System.out.println(i);
		
		i*=3;
		System.out.println(i);
		
		i/=4;
		System.out.println(i);
		
		i%=5;
		System.out.println(i);
		

	}

}
