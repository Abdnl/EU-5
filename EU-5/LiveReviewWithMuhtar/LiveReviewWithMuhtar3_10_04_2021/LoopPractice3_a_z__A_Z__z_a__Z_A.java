package LiveReviewWithMuhtar3_10_04_2021;

public class LoopPractice3_a_z__A_Z__z_a__Z_A {

	public static void main(String[] args) {

		for (char i = 'a'; i <= 'z'; ++i) {
			System.out.print(i + " ");

		}
		System.out.println("\n*************************");

		for (char i = 'A'; i <= 'Z'; ++i) {
			System.out.print(i + " ");

		}
		System.out.println("\n*************************");
		
		for (char i = 'z'; i >= 'a'; i--) {
			System.out.print(i + " ");

		}
		System.out.println("\n*************************");
		
		for (char i = 'Z'; i >= 'A'; i--) {
			System.out.print(i + " ");

		}
	}

}
